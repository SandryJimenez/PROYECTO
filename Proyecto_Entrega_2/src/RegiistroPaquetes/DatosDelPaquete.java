package RegiistroPaquetes;

/**
 *
 * @author JULIO
 */
public class DatosDelPaquete {
    
    private String Sobre;
    private double PesoS;
    private String CajaC;
    private double Peso;
    private double Largo;
    private double Ancho;
    private double Alto;
    private String Otro;
    private String Descripcion;
    private double VolorDeclarado;

    public DatosDelPaquete() {
    }

    public DatosDelPaquete(String Sobre, double PesoS, String CajaC, double Peso, double Largo, double Ancho, double Alto, String Otro, String Descripcion, double VolorDeclarado) {
        this.Sobre = Sobre;
        this.PesoS = PesoS;
        this.CajaC = CajaC;
        this.Peso = Peso;
        this.Largo = Largo;
        this.Ancho = Ancho;
        this.Alto = Alto;
        this.Otro = Otro;
        this.Descripcion = Descripcion;
        this.VolorDeclarado = VolorDeclarado;
    }

    /**
     * @return the Sobre
     */
    public String getSobre() {
        return Sobre;
    }

    /**
     * @param Sobre the Sobre to set
     */
    public void setSobre(String Sobre) {
        this.Sobre = Sobre;
    }

    /**
     * @return the PesoS
     */
    public double getPesoS() {
        return PesoS;
    }

    /**
     * @param PesoS the PesoS to set
     */
    public void setPesoS(double PesoS) {
        this.PesoS = PesoS;
    }

    /**
     * @return the CajaC
     */
    public String getCajaC() {
        return CajaC;
    }

    /**
     * @param CajaC the CajaC to set
     */
    public void setCajaC(String CajaC) {
        this.CajaC = CajaC;
    }

    /**
     * @return the Peso
     */
    public double getPeso() {
        return Peso;
    }

    /**
     * @param Peso the Peso to set
     */
    public void setPeso(double Peso) {
        this.Peso = Peso;
    }

    /**
     * @return the Largo
     */
    public double getLargo() {
        return Largo;
    }

    /**
     * @param Largo the Largo to set
     */
    public void setLargo(double Largo) {
        this.Largo = Largo;
    }

    /**
     * @return the Ancho
     */
    public double getAncho() {
        return Ancho;
    }

    /**
     * @param Ancho the Ancho to set
     */
    public void setAncho(double Ancho) {
        this.Ancho = Ancho;
    }

    /**
     * @return the Alto
     */
    public double getAlto() {
        return Alto;
    }

    /**
     * @param Alto the Alto to set
     */
    public void setAlto(double Alto) {
        this.Alto = Alto;
    }

    /**
     * @return the Otro
     */
    public String getOtro() {
        return Otro;
    }

    /**
     * @param Otro the Otro to set
     */
    public void setOtro(String Otro) {
        this.Otro = Otro;
    }

    /**
     * @return the Descripcion
     */
    public String getDescripcion() {
        return Descripcion;
    }

    /**
     * @param Descripcion the Descripcion to set
     */
    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }

    /**
     * @return the VolorDeclarado
     */
    public double getVolorDeclarado() {
        return VolorDeclarado;
    }

    /**
     * @param VolorDeclarado the VolorDeclarado to set
     */
    public void setVolorDeclarado(double VolorDeclarado) {
        this.VolorDeclarado = VolorDeclarado;
    }
    
    
    
    
    
}
